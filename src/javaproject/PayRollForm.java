    private void wagesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_wagesActionPerformed

        ResultSet rs = null;
        ResultSet rs2 = null;
        Search s = new Search();
        rs = s.find(emp.getText());
        rs2 = s.find2(emp2.getText());
        try {
            if(evt.getSource() == wages && !rs.isBeforeFirst() && !rs2.isBeforeFirst()){
                JOptionPane.showMessageDialog(null, "No Employee Info for '" + emp.getText() + emp2.getText()+ "' ", "Error",JOptionPane.ERROR_MESSAGE);
                txtweight.setText("");
                txtbasic.setText("");
            }
            else{
            
             if(!rs.isBeforeFirst() && jobTitle.getSelectedItem().equals("Lecturer") && schedule.getSelectedItem().equals("Full time")){
                JOptionPane.showMessageDialog(null, "You are not a lecturer !","Eror",JOptionPane.ERROR_MESSAGE);
            }
            
           else if(!rs.isBeforeFirst() && jobTitle.getSelectedItem().equals("Lecturer") && schedule.getSelectedItem().equals("Part time")){
                JOptionPane.showMessageDialog(null, "You are not a lecturer !","Eror",JOptionPane.ERROR_MESSAGE);
           } 
             
           else if(rs.isBeforeFirst() && !(jobTitle.getSelectedItem().equals("Lecturer"))){
                JOptionPane.showMessageDialog(null, "Access Denied !","Eror",JOptionPane.ERROR_MESSAGE);
                txtbasic.setText("");
                txtpension.setText("");
                txtweight.setText("");
                txttax.setText("");
        
                gross.setText("");
                deduc.setText("");
                netpay.setText("");
       
                jobTitle.setSelectedItem("Select one...");
                schedule.setSelectedItem("Select one...");
           }  
            }
    
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, "Error");
        }
   
       double enterWeight = Double.parseDouble(txtweight.getText());
       double enterBasic = Double.parseDouble(txtbasic.getText());
       double Gross = enterWeight + enterBasic;
       String result = String.format("%.2f", Gross);
       gross.setText(result);
       gross.setText("UGX. " + result);
       
       double tax = 0.09 * Gross;
       String result2 = String.format("%.2f", tax);
       txttax.setText(result2);
       txttax.setText("UGX. " + result2);
       
       double deductions = Gross - tax;
       String result3 = String.format("%.2f", deductions);
       deduc.setText(result3);
       deduc.setText("UGX. " + result3);
       
       double pension = deductions * 0.12;
       String result4 = String.format("%.2f", pension);
       txtpension.setText(result4);
       txtpension.setText("UGX. " + result4);
       
       double totalDeductions = tax + pension;
       double netPay = Gross - totalDeductions;
       String result5 = String.format("%.2f", netPay);
       netpay.setText(result5);
       netpay.setText("UGX. " + result5);

        
    }//GEN-LAST:event_wagesActionPerformed

