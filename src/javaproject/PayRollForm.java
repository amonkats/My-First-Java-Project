    private void empKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_empKeyPressed
       
        ResultSet rs = null;
        Search s = new Search();
        rs = s.find(emp.getText());
        
        try {
            
            if(!rs.isBeforeFirst() && evt.getKeyCode() == KeyEvent.VK_ENTER){
                JOptionPane.showMessageDialog(null, "Not an employee at IUEA !", "Error",JOptionPane.ERROR_MESSAGE);
                address.setText("");
                faculty.setText("");
                id.setText("");
                
            }
            
            else{
                if(rs.next()){
                
              if(evt.getKeyCode() == KeyEvent.VK_ENTER){
                emp.setText(rs.getString("Employee_Name"));
                address.setText(rs.getString("Address"));
                faculty.setText(rs.getString("Faculty"));
                id.setText(rs.getString("Lecturer_ID"));
            }
    
            }
       }
           
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, "Connection Failed !");
        }
        
        
    }//GEN-LAST:event_empKeyPressed

